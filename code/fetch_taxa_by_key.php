function fetch_name($key)
{
	$name = '';
	
	$url = "https://biodiversity.org.au/afd/taxa/$key";

	$opts = array(
	  CURLOPT_URL =>$url,
	  CURLOPT_FOLLOWLOCATION => TRUE,
	  CURLOPT_RETURNTRANSFER => TRUE
	);
	
	$ch = curl_init();
	curl_setopt_array($ch, $opts);
	$data = curl_exec($ch);
	$info = curl_getinfo($ch); 
	curl_close($ch);
	
	if (preg_match('/<a href="([^\/]+)\/checklist">/', $data, $m))
	{
		$name = $m[1];
	}

	return $name;
}	

